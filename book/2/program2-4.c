#include <stdio.h>

double slow_power(double x, int n) {
	int i;
	double r=1.0;
	for (i=0; i<n; i++) {
		r = r * x;
	}
	return (r);
}

int main() {
	double x;
	int n;
	scanf("%lf %d", &x, &n);
	double a = slow_power(x, n);
	printf("%.1lf", a);
}