// ***********************************************************************************************************************************************************************************
// 반복적인 피보나치 수열 계산 프로그램(반복 구조)
// ***********************************************************************************************************************************************************************************
#include <stdio.h>

int fib_iter(int n) {
	if (n<2) return n;
	else {
		int i, tmp, current=1, last=0;
		for (i=2; i<=n; i++) {
			tmp = current;
			current += last;
			last = tmp;
		}
		return current;
	}
}

int main() {
	int n;
	scanf("%d", &n);
	int a = fib_iter(n);
	printf("%d", a);
}