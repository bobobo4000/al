// 문제
// ***********************************************************************************************************************************************************************************
// 20 이하의 정수 n을 입력받고 n명의 점수를 입력받아 높은 점수부터 차례로 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// 5
// 35 10 35 100 64
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// 100
// 64
// 35
// 35
// 10
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int num[20];
	int n;
	int i, j, tmp;

	scanf("%d", &n);

	for (i=0; i<n; i++) {
		scanf("%d", &num[i]);	
	}

	for (i=0; i<n-1; i++) {
		for (j=i+1; j<n; j++) {
			if (num[i] < num[j]) {
				tmp = num[i];
				num[i] = num[j];
				num[j] = tmp;
			}
		}
	}
	for (i=0; i<n; i++) {
		printf("%d\n", num[i]);
	}

	

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}