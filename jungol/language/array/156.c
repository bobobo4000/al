// 문제
// ***********************************************************************************************************************************************************************************
// 세 자리 이하의 정수를 차례로 입력 받다가 999가 입력되면 프로그램을 종료하고 그 때까지 입력된 최대값과 최소값을 출력하는 프로그램을 작성하시오.
// 입력받는 정수는 100개 이하이다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// 45 19 123 58 10 -55 16 -1 999
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// max : 123
// min L -55
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int num[100];
	int i, j;
	int max = -1000;
	int min = 1000;

	for (i=0; i<100; i++) {
		scanf("%d", &num[i]);
		
		if (num[i] == 999) {	
			break;
		}
		
		if (num[i] > max) {
			max = num[i];
		}
		if (num[i] < min) {
			min = num[i];
		}

	}

	printf("max : %d\nmin : %d", max, min);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}