// 10개의 문자를 입력받아서 첫 번째 네 번째 일곱 번째 입력받은 문자를 차례로 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// A B C D E F G H I J																						 
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// A D G																						 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 16 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	char str[10];

	for (int i=0; i<10; i++) {
		scanf(" %c", &str[i]);
	}

	printf("%c %c %c", str[0], str[3], str[6]);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}