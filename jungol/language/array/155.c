// 문제
// ***********************************************************************************************************************************************************************************
// 6개의 문자배열을 만들고 {'J', 'U', 'N', 'G', 'O', 'L'} 으로 초기화 한 후 문자 한 개를 입력받아 배열에서의 위치를 출력하는 프로그램을 작성하시오.
// 첫 번째 위치는 0번이며 배열에 없는 문자가 입력되면 "none" 라는 메시지를 출력하고 끝내는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// ㅣ 
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// 5
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 초
// 메모리 제한 :  MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	char str[6] = {'J', 'U', 'N', 'G', 'O', 'L'};
	char b;
	int i;

	scanf(" %c", &b);

	for (i=0; i<6; i++) {	
		if (str[i] == b) {
			printf("%d\n", i);
		} 
	}
	printf("none\n");

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}