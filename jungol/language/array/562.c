// 문제
// ***********************************************************************************************************************************************************************************
// 10개의 정수를 입력받아 배열에 저장한 후 짝수 번째 입력된 값의 합과 홀수 번째 입력된 값의 평균을 출력하는 프로그램을 작성하시오.
// 평균은 반올림하여 소수 첫째자리까지 출력한다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// 95 100 88 65 76 89 58 93 77 99																						 
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// sum: 446, avg: 78.8																						 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int w[10];
	int i, j;
	int odd, add = 0;
	int oddcnt = 0;
	float oddavg = 0;

	for (i=0; i<10; i++) {
		scanf("%d", &w[i]);
	}

	for (j=1; j<=10; j++) {
		if (j%2 != 0) {
			odd += w[j-1];
			oddcnt++;
		} else {
			add += w[j-1];

		}
	}

	oddavg = (float)odd/oddcnt;

	printf("sum: %d\navg: %.1f", add, oddavg);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}