// 문제
// ***********************************************************************************************************************************************************************************
// 문자 10개를 저장할 수 있는 배열을 만들고 10개의 문자를 입력받아 입력받은 문자를 이어서 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// A B C D E F G H I J																					 
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// ABCDEFGHIJ																						 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 16 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	char str[10] = {0};

	for (int i=0; i<10; i++) {
		scanf(" %c", &str[i]);
	}
	for (int i=0; i<10; i++) {
		printf("%c", str[i]);
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}