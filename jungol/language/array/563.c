// 문제
// ***********************************************************************************************************************************************************************************
// 10개의 정수를 입력받아 배열에 저장한 후 내림차순으로 정렬하여 출력하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// 95 100 88 65 76 89 58 93 77 99																						 
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// 100 99 95 93 89 88 77 76 65 58																						 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int w[10];
	int tmp, i, j;

	for (i=0; i<10; i++) {
		scanf("%d", &w[i]);
	}
	for (i=0; i<9; i++) {
		for (j=i+1; j<10; j++) {
			if (w[i] < w[j]) {
				tmp = w[i];
				w[i] = w[j];
				w[j] = tmp;
			}
		}
	}

	for (i=0; i<10; i++) {
		printf("%d ", w[i]);
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}
