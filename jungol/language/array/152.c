// 문제
// ***********************************************************************************************************************************************************************************
// 10개의 정수를 입력받아 홀수 번째 입력받은 정수의 합과 짝수 번째 입력받은 정수의 합을 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// 10 20 25 66 83 7 22 90 1 100																						 
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// odd: 141
// even: 283																						 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int w[10];
	int odd = 0;
	int even = 0;
	int i;

	for (i=0; i<10; i++) {
		scanf("%d", &w[i]);
	}

	for (i=1; i<=10; i++) {
		if (i%2 != 0) {
			odd += w[i-1];
		} else {
			even += w[i-1];
		}
	}

	printf("odd: %d\neven: %d", odd, even);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}