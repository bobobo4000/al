// 문제
// ***********************************************************************************************************************************************************************************
// 10개의 정수를 입력받아 100미만의 수 중 가장 큰 수와 100 이상의 수 중 가장 작은 수를 출력하는 프로그램을 작성하시오.
// (입력되는 정수의 범위는 1이상 10,000 미만이다. 만약 해당하는 수가 없을 때에는 100 을 출력한다.)
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// 88 123 659 15 443 1 99 313 105 48																						 
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// 99 105																						 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

  int w[10];
	int i;
	int max = -10000;
	int min = 10000;

	for (i=0; i<10; i++) {
		scanf("%d", &w[i]);

		if (w[i] < 100) {
			if (w[i] > max) {
				max = w[i];
			}
		} else {
			if (w[i] < min) {
				min = w[i];
			}
		}
	}

	if (max == -10000) {
		max = 100;
	} else if (min == 10000) {
		min = 100;
	}

	printf("100미만의 값 중 가장 큰 값 = %d, 100이상의 값 중 가장 작은 값 = %d", max, min);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

// 10개의 정수 입력
// 배열의 값이 100 미만인 경우와 100이상인 경우로 나뉨
// 100미만의 값 중 가장 큰 값 구하기
// 100이상의 값 중 가장 작은 값 구하기
// 100미만 값이나 100이상 값 중 해당되는 값이 없다면 100을 출력