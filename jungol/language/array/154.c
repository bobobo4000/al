// 문제
// ***********************************************************************************************************************************************************************************
// 6명의 몸무게를 입력받아 몸무게의 평균을 출력하는 프로그램을 작성하시오.
// 출력은 반올림하여 소수 첫째자리까지로 한다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// 23.2 39.6 66.4 50.0 45.6 48.0 
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// 45.5							 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	float w[6];
	int i, j;
	float sum=0;
	int cnt=0;
	float avg=0;

	for (i=0; i<6; i++) {
		scanf("%f", &w[i]);
	}
	for (j=0; j<6; j++) {
		sum += w[j];
		cnt++;
	}

	avg = (float)sum/cnt;
	printf("%.1f", avg);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}