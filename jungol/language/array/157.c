// 문제
// ***********************************************************************************************************************************************************************************
// 100개 이하의 정수를 입력받다가 0이 입력되면 그 때까지 입력된 정수 중 5의 배수의 개수와 합계 평균을 출력하는 프로그램을 작성하시오.
// 평균은 소수점 이하 1자리까지 출력한다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// 35 10 23 100 64 51 5 0
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// Multiples of 5 : 4
// sum : 150
// avg : 37.5
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int num[100];
	int i, j;
	int cnt=0;
	int sum=0;
	float avg=0;


	for (i=0; i<100; i++) {
		scanf("%d", &num[i]);

		if (num[i] == 0) {
			break;
		}
	}

	for (j=0; j<i; j++) {
		if (num[j]%5 == 0) {
			cnt++;
			sum += num[j];
		}
	}
	
	avg = (float)sum/cnt;
	printf("Multiples of 5 : %d\nsum : %d\navg : %.1f", cnt, sum, avg);



	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}