// 문제
// ***********************************************************************************************************************************************************************************
// 정수를 입력받아서 3의 배수가 아닌 경우에는 아무 작업도 하지 않고 3의 배수인 경우에는 3으로 나눈 몫을 출력하는 작업을 반복하다가
// -1이 입력되면 종료하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
//																						 |
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int num;


	while(1) {
		scanf("%d", &num);

		if (num%3 == 0) {
			printf("%d\n", num/3);
		}

		if (num == -1) {
			printf("프로그램을 종료합니다.");
			break;
		}
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}