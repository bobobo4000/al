// 문제
// ***********************************************************************************************************************************************************************************
// 0 이상의 정수들이 공백으로 구분되어 반복적으로 주어진다.
// 0이 입력되면 반복문을 멈추고 그 전까지 입력받은 수들에 대하여
// 홀수의 개수와 짝수의 개수를 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 9 7 10 5 33 65 0														 | odd : 5
//																						 | even : 1
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int num;
	int odd=0;
	int even=0;

	while(1) {
		scanf("%d", &num);

		if (num%2 != 0) {
			odd++;
		} else {
			even++;
			if (num == 0) {
				even--;
			}
		}

		if (num == 0) {
			break;
		}
	}

	printf("odd : %d, even : %d", odd, even);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}