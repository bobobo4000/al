// 문제
// ***********************************************************************************************************************************************************************************
// 100 이하의 양의 정수만 입력된다. while 문을 이용하여 1부터 입력받은 정수까지의 합을 구하여 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 10																					 | 55
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;
	int i=1;
	int sum = 0;

	printf("정수를 입력하세요: ");
	scanf("%d", &n);

	if (n > 100 || n < 1) {
		printf("1 ~ 100 사이의 값을 입력하세요.");
	} else {
		while (i <= n) {
			sum += i++;
		}

		printf("%d", sum);
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

// 1. 100이하의 정수를 입력
// 2. 1~n까지의 합을 출력