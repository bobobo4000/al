// 문제
// ***********************************************************************************************************************************************************************************
// 한 개의 정수를 입력받아 양수(positive integer)인지 음수(negative number)인지 출력하는 작업을 반복하다가 0이 입력되면 종료하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// number? 10																	 | positive integer
// number? -10																 | negative number
// number? 0																	 |
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;

	while(1) {
		printf("number? ");
		scanf("%d", &n);

		if (n > 0) {
			printf("positive integer\n");
		} else if (n < 0) {
			printf("negative number\n");
		} else {
			break;
		}
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}