// 문제
// ***********************************************************************************************************************************************************************************
// 0 부터 100 까지의 점수를 계속 입력받다가 범위를 벗어나는 수가 입력되면 그 이전까지 입력된 자료의 합계와 평균을 구하는 프로그램을 작성하시오.
// (평균은 반올림하여 소수 첫째자리까지 출력한다.)
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 55 100 48 36 0 101													 | sum : 239, avg : 47.8
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int num;
	int sum = 0;
	int cnt = 0;

	while(1) {
		scanf("%d", &num);

		if (0 > num || 100 < num) {
			break;
		} else {
			sum += num;
			cnt ++;
		}
	}
	float avg = (float)sum/cnt;
	printf("sum : %d, avg : %.1f", sum, avg);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}