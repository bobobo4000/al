// 문제
// ***********************************************************************************************************************************************************************************
// 정수를 계속 입력받다가 100 이상의 수가 입력이 되면 마지막 입력된 수를 포함하여 합계와 평균을 출력하는 프로그램을 작성하시오.
// (평균은 반올림하여 소수 첫째자리까지 출력한다.)
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 1 2 3 100																	 | 106
//																						 | 26.5
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int num;
	int cnt=0;
	int sum=0;
	float avg=0;

	while(1) {
		scanf("%d", &num);

		if (num < 100) {
			sum += num;
			cnt++;
		} else {
			sum += num;
			cnt++;
			break;
		}
	}

	avg = (float)sum/cnt;

	printf("%d\n", sum);
	printf("%.1f", avg);	

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}