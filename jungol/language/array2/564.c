// 문제
// ***********************************************************************************************************************************************************************************
// 영문 대문자를 입력받다가 대문자 이외의 문자가 입력되면 입력을 중단하고 
// 영문 대문자들에 대하여 1번 이상 입렫된 각 문자와 그 문자의 개수를 사전 순으로 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// A B C F F F B Z !
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// A : 1
// B : 2
// C : 1
// F : 3
// Z : 1
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int a[26] = {0};
  int i;
  char input;
    
	for(i=0;i<100;i++){
    scanf(" %c", &input);
    
		if(input < 65 || 90 < input) break;
    a[input-65]++;
  }

  for(i=0;i<26;i++){
    if(a[i]>0){
      printf("%c : %d\n", 'A'+i, a[i]);
    }
  }

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}