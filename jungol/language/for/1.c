// ***********************************************************************************************************************************************************************************
// 문자를 입력받아서 입력받은 문자를 20번 반복하여 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// A																					 | AAAAAAAAAAAAAAAAAAAA																					 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int i;
	char c[30];

	printf("문자를 입력하세요: ");
	scanf(" %s", &c);

	for (i=0; i<20; i++) {
		printf("%s", c);
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}