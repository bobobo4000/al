// 문제
// ***********************************************************************************************************************************************************************************
// 행과 열의 수를 입력받아 다음과 같이 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 3 4																				 | 1 2 3 4
//																						 | 2 4 6 8
//																						 | 3 6 9 12
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 초
// 메모리 제한 : MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int x, y;

	printf("두 정수를 입력하세요: ");
	scanf("%d %d", &x, &y);

	for (int i=1; i<=x; i++) {
		if (i==1) {
			for (int j=0; j<y; j++) {
				printf("%d ", j+1);
			}
		} else {
			for (int m=1; m<=y; m++) {
				printf("%d ", m*i);
			}
		}
		printf("\n");
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}
