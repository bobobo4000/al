// 문제
// ***********************************************************************************************************************************************************************************
// 10 이하의 자연수 n을 입력받아 "JUNGOL"을 n번 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 4																					 | JUNGOL
//																						 | JUNGOL
//																						 | JUNGOL
//																						 | JUNGOL
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;

	printf("정수를 입력하세요: ");
	scanf("%d", &n);

	for (int i=0; i<n; i++) {
		printf("JUNGOL\n");
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}