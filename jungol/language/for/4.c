// 문제
// ***********************************************************************************************************************************************************************************
// 100 이하의 두 개의 정수를 입력받아 작은 수부터 큰 수까지 차례대로 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 10 5																				 | 5 6 7 8 9 10
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int x, y;

	printf("두 정수를 입력하세요: ");
	scanf("%d %d", &x, &y);

	if (x < y) {
		for (int i=x; i<=y; i++) {
			printf("%d ", i);
		}
	} else {
		for (int i=y; i<=x; i++) {
			printf("%d ", i);
		}
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}