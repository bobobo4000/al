// 문제
// ***********************************************************************************************************************************************************************************
// 1~12 사이의 정수를 입력받아 평년의 경우 해당 월의 날수를 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 2																					 | 28
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int month;

	printf("월을 입력하시오: ");
	scanf("%d", &month);

	if (1 > month || month > 12) {
		printf("월은 1 ~ 12사이의 값 입니다.");		
	} else {
		if (month == 2) {
			printf("28");
		} else if (month == 4 || month == 6 || month == 9 || month == 11) {
			printf("30");
		} else {
			printf("31");
		}
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

// 1. 월을 입력
// 2. 해당 월의 일수를 출력