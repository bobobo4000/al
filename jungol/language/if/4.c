// 문제
// ***********************************************************************************************************************************************************************************
// 정수를 입력받아 0 이면 "zero" 양수이면 "plus" 음수이면 "minus" 라고 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 0																					 | zero
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 초
// 메모리 제한 : MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int x;

	printf("정수를 입력하시오: ");
	scanf("%d", &x);

	if (x<0) {
		printf("minus");
	} else if (x > 0) {
		printf("plus");
	} else {
		printf("zero");
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

// 1. 정수를 입력
// 2. 0이면 zero 출력
// 3. 양수이면 plus 출력
// 4. 음수이면 minus 출력