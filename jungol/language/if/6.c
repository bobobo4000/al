// 문제
// ***********************************************************************************************************************************************************************************
// 1번은 개, 2번은 고양이, 3번은 병아리로 정하고 번호를 입력하면 번호에 해당하는 동물을 영어로 출력하는 프로그램을 작성하시오.
// 해당 번호가 없으면 "I don't know"라고 출력한다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// Number? 2																	 | cat
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int num;

	printf("Number? ");
	scanf("%d", &num);

	if (num == 1) {
		printf("dog");
	} else if (num == 2) {
		printf("cat");
	} else if (num == 3) {
		printf("chick");
	} else {
		printf("I don't know");
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

// 1. 정수를 입력
// 2. 1~3은 조건에 따른 출력
// 3. 그 이외의 정수는 "I don't know" 출력