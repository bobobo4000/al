// 문제
// ***********************************************************************************************************************************************************************************
// 년도를 입력받아 윤년(leap year)인지 평년(common year)인지 판단하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 2008																				 | leap year
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 0 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int year;

	printf("년도를 입력하시오: ");
	scanf("%d", &year);

	if (year%400 == 0) {
		printf("leap year");
	} else if (year%4 == 0 && year != 100) {
		printf("leap year");
	} else {
		printf("common year");
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

// 1. 년도를 입력
// 2. 400으로 나누어 떨어지면 윤년 아니면 평년
// 3. 또는 4로 나누어 떨어지고 100으로 나누어 떨어지지 않으면 윤년, 아니면 평년
// 4. 조건에 따라 윤년인지 평년인지 출력