// 문제
// ***********************************************************************************************************************************************************************************
// 상근날드에서 가장 잘 팔리는 메뉴는 세트 메뉴이다. 주문할 때, 자신이 원하는 햄버거와 음료를 하나씩 골라, 세트로 구매하면, 가격의 합계에서 50원을 뺀 가격이 세트 메뉴의 가격이 된다.
// 햄버거는 총 3종류 상덕버거, 중덕버거, 하덕버거가 있고, 음료는 콜라와 사이다 두 종류가 있다.
// 햄버거와 음료의 가격이 주어졌을 때, 가장 싼 세트 메뉴의 가격을 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 입력은 총 다섯 줄이다. 첫째 줄에는 상덕버거, 둘째 줄에는 중덕버거, 셋째 줄에는 하덕버거의 가격이 주어진다.
// 넷째 줄에는 콜라의 가격, 다섯째 줄에는 사이다의 가격이 주어진다.
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 첫째줄에 가장 싼 세트 메뉴의 가격을 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// 모든 가격은 100원이상, 2000원 이하이다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 800																				 | 848
// 700																				 |
// 900																				 |
// 198																				 |
// 330																				 |
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 128MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int burger[3];
	int soda[2];
	int i, j, temp, set=4000;
	printf("상덕버거, 중덕버거, 하덕버거, 콜라, 사이다 순으로 가격을 입력하세요: \n");
	for(i=0; i<3; i++) {
		scanf("%d", &burger[i]);
	}
	for(j=0; j<2; j++) {
		scanf("%d", &soda[j]);
	}
	for(i=0; i<3; i++) {
		for(j=0; j<2; j++) {
			temp = burger[i] + soda[j] - 50;
			if (temp < set) {
				set = temp;
			}
		}
	}

	printf("%d", set);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}