// 문제
// ***********************************************************************************************************************************************************************************
// 두 정수 A와 B를 입력받은 다음, A+B를 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 테스트 케이스의 개수 T가 주어진다.
// 각 테스트 케이스는 한 줄로 이루어져 있으며, 각 줄에 A와 B가 주어진다.
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 각 테스트 케이스마다 A+B를 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// 0 < A, B < 10
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 5																					 | 
// 1 1																				 | 2
// 2 3 																				 | 5
// 3 4																				 | 7
// 9 8																				 | 17
// 5 2																				 | 7
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 256MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int x, y, t, i;

	printf("테스트 케이스의 개수를 입력하세요: ");
	scanf("%d", &t);

	for (i=0; i<t; i++) {
		scanf("%d %d", &x, &y);
		printf("입력된 두 수의 합 = %d\n", x+y);
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}