// 문제
// ***********************************************************************************************************************************************************************************
// 첫째 줄에는 별 1개, 둘째 줄에는 별 2개, N번재 줄에는 별 N개를 찍는 문제
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 N이 주어진다.
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 첫째 줄부터 N번째 줄까지 차례대로 별을 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// 1 <= N <= 100
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 3																					 | *
//  																					 | **
//  																					 | ***
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 128MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n, i, j;

	printf("정수를 입력하세요: ");
	scanf("%d", &n);

	for (i=1; i<=n; i++) {
		for (j=1; j<=i; j++) {
			printf("*");
		}
		printf("\n");
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n소요된 시간 : %.3f \n", res);
}