// 문제
// ***********************************************************************************************************************************************************************************
// N을 입력받은 뒤, 구구단 N단을 출력하는 프로그램을 작성하시오. 출력 형식에 맞춰서 출력하면 된다.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 N이 주어진다.
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 출력형식과 같게 N*1 부터 N*9까지 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// 1 <= N <= 9
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 2																					 | 2 * 1 = 2
//																						 | 2 * 2 = 4
//																						 | ....
//																						 | 2 * 9 = 18
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 초
// 메모리 제한 : MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n, i;

	printf("정수를 입력하세요(1~9사이): ");
	scanf("%d", &n);

	if (n < 1 || n > 9) {
		printf("1~9사이 값만 입력 가능합니다.");
	} else {
		for(i=1; i<10; i++) {
			printf("%d * %d = %d\n", n, i, n*i);
		}
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}