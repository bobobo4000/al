// 문제
// ****************************************************************************************************
// (세 자리 수) × (세 자리 수)는 다음과 같은 과정을 통하여 이루어진다.
//                 4 7 2 ··········(1)
//               * 3 8 5 ··········(2)
// -----------------------------------
//               2 3 6 0 ··········(3)
//             3 7 7 6   ··········(4)
//           1 4 1 6     ··········(5)
// -----------------------------------
//           1 8 1 7 2 0 ··········(6)
// (1)과 (2)위치에 들어갈 세 자리 자연수가 주어질 때 (3), (4), (5), (6)위치에 들어갈 값을 구하는 프로그램을 작성하시오.
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫째 줄에 (1)의 위치에 들어갈 세 자리 자연수가, 둘째 줄에 (2)의 위치에 들어갈 세자리 자연수가 주어진다.
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 첫째 줄부터 넷째 줄까지 차례대로 (3), (4), (5), (6)에 들어갈 값을 출력한다.
// ****************************************************************************************************

// 예제 입력																	 | 예제 출력
// ****************************************************************************************************
// 472																				| 2336
// 385																				| 3776
//																					  | 1416
// 																						| 181720
// ****************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 128MB

#include "header1.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

  int A, B;
	int z, x, c;

	100 <= A, B <= 999;

	scanf("%d %d", &A, &B);

  z = B / 100;
	B %= 100;

	x = B / 10;
	B %= 10;

	c = B % 10;

	B = z*100 + x*10 + c;

	printf("(3)출력값 : %d \n", A*c);
	printf("(4)출력값 : %d \n", A*x);
	printf("(5)출력값 : %d \n", A*z);
	printf("(6)출력값 : %d ", A*B);
    
	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n소요된 시간 : %.3f \n", res);
}