// 문제
// ****************************************************************************************************
// 자연수 N이 주어졌을 때, N부터 1까지 한 줄에 하나씩 출력하는 프로그램을 작성하시오
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫째 줄에 100,000보다 작거나 같은 자연수 N이 주어진다.
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 첫째 줄부터 N번째 줄 까지 차례대로 출력한다.
// ****************************************************************************************************

// 제한
// ****************************************************************************************************
// 
// ****************************************************************************************************

// 예제 입력																		| 예제 출력
// ****************************************************************************************************
// 5																					 | 5
//  																					 | 4
//  																					 | 3
//  																					 | 2
//  																					 | 1
// ****************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 128MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;
	int i;

	printf("n을 입력하세요: ");
	scanf("%d", &n);

	for(i=n; i>0; i--) {
		printf("%d\n", i);
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n소요된 시간 : %.3f \n", res);
}