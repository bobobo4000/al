// 문제
// ****************************************************************************************************
// N개의 정수가 주어진다. 이때, 최솟값과 최댓값을 구하는 프로그램을 작성하시오.
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫째 줄에 정수의 개수 N이 주어진다. 둘째 줄에는 N개의 정수를 공백으로 구분해서 주어진다.
// 모든 정수는 -1,000,000보다 크거나 같고, 1,000,000보다 작거나 같은 정수이다.
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 첫째 줄에 주어진 정수 N개의 최솟값과 최댓값을 공백으로 구분해 출력한다.
// ****************************************************************************************************

// 제한
// ****************************************************************************************************
// 1 <= N <= 1,000,000
// ****************************************************************************************************

// 예제 입력																		| 예제 출력
// ****************************************************************************************************
// 5																					 | 7 35
// 20 10 35 30 7															 |
// ****************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 256MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;
	int z;

	int max=-1000000;
	int min=1000000;
	
	printf("정수를 입력하세요: ");
	scanf("%d", &n);

	for (int i=0; i<n; i++) {
		scanf("%d", &z);
		if (z < min) {
			min = z;
		}
		if (z > max) {
			max = z;
		}
	}

	printf("최솟값: %d, 최댓값: %d", min, max);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}