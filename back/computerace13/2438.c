// 문제
// ****************************************************************************************************
// 첫째 줄에는 별 1개, 둘째 줄에는 별 2개, N번째 줄에는 N개를 찍는 문제
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫째 줄에 N이 주어진다.
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 첫째 줄부터 N번째 줄까지 차례대로 별을 출력한다.
// ****************************************************************************************************

// 제한
// ****************************************************************************************************
// 1 <= N <= 100
// ****************************************************************************************************

// 예제 입력																		| 예제 출력
// ****************************************************************************************************
// 3	 																				 | *
//																						 | **
//																						 | ***
// ****************************************************************************************************

// 시간 제한 : 초
// 메모리 제한 : MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;
	int i;
	int j;

 	while(1) {
		printf("정수를 입력하세요(1~100사이 값): ");
		scanf("%d", &n);

		if (1> n || 100 < n) {
			printf("1에서 100사이 값만 입력하세요!!\n");
		} else {
			for(i=0; i<n; i++) {
				for(j=0; j<=i; j++) {
					printf("*");
				}
				printf("\n");
			}
			break;
		}
 }

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n소요된 시간 : %.3f \n", res);
}