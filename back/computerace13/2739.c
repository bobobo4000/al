// 문제
// ****************************************************************************************************
// N을 입력받은 뒤, 구구단 N단을 출력하는 프로그램을 작성하시오. 출력 형식에 맞춰서 출력하면 된다.
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫째 줄에 N이 주어진다. 
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 출력형식과 같게 N*1부터 N*9까지 출력한다.
// ****************************************************************************************************

// 제한
// ****************************************************************************************************
// N은 1보다 크거나 같고, 9보다 작거나 같다.
// ****************************************************************************************************

// 예제 입력																		| 예제 출력
// ****************************************************************************************************
// 2																					 | 2 * 1 = 2 ...... 2 * 9 = 18
// ****************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 128 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;
	int i;

	printf("정수를 입력하세요(1 ~ 9): ");
	scanf("%d", &n);

	for(i=1; i<10; i++) {
		printf("%d * %d = %d\n", n, i, n*i);
	}
	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n소요된 시간 : %.3f \n", res);
}