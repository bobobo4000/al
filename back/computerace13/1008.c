// 문제
// ****************************************************************************************************
// 두 정수 A와 B를 입력받은 다음, A/B를 출력하는 프로그램을 작성하시오.
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫째 줄에 A와 B가 주어진다.
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 첫째 줄에 A/B를 출력한다. 실제 정답과 출력값의 절대오차 또는 상대오차가 (10의 -9) 이하이면 정답이다.
// ****************************************************************************************************

// 제한
// ****************************************************************************************************
// (0 < A, B < 10)
// ****************************************************************************************************

// 예제 입력																		| 예제 출력
// ****************************************************************************************************
// 4 5																				 | 0.8
// 1 3																				 | 0.333333333333333333333
// ****************************************************************************************************

// 시간 제한 : 2초
// 메모리 제한 : 128 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int a, b;
	float c;

	while (1) {
		printf("a를 입력하세요: ");
		scanf("%d", &a);

		if (0>=a || 10<=a) {
			printf("a는 1 ~ 9 사이의 값만 가능합니다. \n");
		} else {
			break;
		}
	}

	while (1) {
		printf("b를 입력하세요: ");
		scanf("%d", &b);

		if (0>=b || 10<=b) {
			printf("b는 1 ~ 9 사이의 값만 가능합니다.\n");
		} else {
			c = (float)a/b;
			printf("%f", c);
			break;
		}
	}
	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n소요된 시간 : %.3f \n", res);
}