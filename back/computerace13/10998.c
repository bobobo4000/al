// 문제
// ****************************************************************************************************
// 두 정수 A와 B를 입력받은 다음, A×B를 출력하는 프로그램을 작성하시오.
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫째 줄에 A와 B가 주어진다.
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 첫째 줄에 AxB를 출력한다.
// ****************************************************************************************************

// 제한
// ****************************************************************************************************
// (0 < A, B < 10)
// ****************************************************************************************************

// 예제 입력																		| 예제 출력
// ****************************************************************************************************
// 3 4																				 | 12
// ****************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 256 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int a, b;

	while(1) {
	printf("a를 입력하세요: ");
	scanf("%d", &a);
		if (0>= a || 10 <= a) {
			printf("a의 값이 1 ~ 9 사이의 값이어야 합니다.\n");
		} else {
			break;
		}
	}

	while(1) {
		printf("b를 입력하세요: ");
		scanf("%d", &b);
			if (0>= b || 10 <= b) {
				printf("b의 값이 1 ~ 9 사이의 값이어야 합니다.\n");
			} else {
				printf("a x b = %d", a*b);
				break;
			}
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n소요된 시간 : %.3f \n", res);
}