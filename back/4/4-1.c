// 문제
// ***********************************************************************************************************************************************************************************
// 두 정수 A와 B를 입력받은 다음, A+B를 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 입력은 여러 개의 테스트 케이스로 이루어져 있다.
// 각 테스트 케이스는 한 줄로 이루어져 있으며, 각 줄에 A와 B가 주어진다.
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 각 테스트 케이스마다 A+B를 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// 0 < A, B < 10
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 1 1																				 | 2
// 2 3																				 | 5
// 3 4																				 | 7
// 9 8																				 | 17
// 5 2																				 | 7
// 0 0																				 | 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 256MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int x, y;

	while(1) {
		printf("두 정수를 입력하세요: ");
		scanf("%d %d", &x, &y);

		printf("두 정수의 합: %d\n", x+y);
		if (x == 0 && y == 0) {
			break;
		}
	}

	printf("프로그램을 종료합니다.");

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}