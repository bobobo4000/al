// 문제
// ***********************************************************************************************************************************************************************************
// 0보다 크거나 같고, 99보다 작거나 같은 정수가 주어질 때 다음과 같은 연산을 할 수 있다. 먼저 주어진 수가 10보다 작다면 앞에 0을 붙여 두 자리 수로 만들고, 각 자리의 숫자를 더한다.
// 그 다음, 주어진 수의 가장 오른쪽 자리 수와 앞에서 구한 합의 가장 오른쪽 자리 수를 이어 붙이면 새로운 수를 만들 수 있다. 다음 예를 들어보자.
// - 26부터 시작한다. 2+6=8이다. 새로운 수는 68이다. 6+8=14이다. 새로운 수는 84이다. 8+4 = 12이다. 새로운 수는 42이다. 4+2 = 6이다. 새로운 수는 26이다.
// - 위의 예는 4번만에 원래 수로 돌아올 수 있다. 따라서 26의 사이클의 길이는 4이다.
// - N이 주어졌을 때, N의 사이클의 길이를 구하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 N이 주어진다. 
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 N의 사이클 길이를 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// N은 0보다 크거나 같고, 99보다 작거나 같은 정수이다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 26																				 	 | 4
// 55																					 | 3
// 1																					 | 60 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 256MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n=0, m=0, x, y, z;
	int cnt=0;

	printf("정수를 입력하세요: ");
	scanf("%d", &n);
	m = n;

	while(1) {
		if (m < 10) {
			x = 0;
		} else {
			x = m/10;
		}
		y = m%10;

		z = x + y; // 새로운 수
		cnt++;

		x = m%10;
		y = z%10;
		m = (x*10)+y;

		if (m == n) {
			break;
		}
	}

	printf("사이클 횟수: %d", cnt);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}