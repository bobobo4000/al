// 문제
// ***********************************************************************************************************************************************************************************
// N개의 숫자가 공백 없이 쓰여있다. 이 숫자를 모두 합해서 출력하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 숫자의 개수 N이 주어진다.
// 둘째 줄에 숫자 N개가 공백없이 주어진다.
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 입력으로 주어진 숫자 N개의 합을 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// 1 <= N <= 100
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 1																					 | 1
// 1																					 | 
//
// 5																					 | 15 
// 54321																			 | 
// 
// 25																					 | 7
// 7000000000000000000000											 | 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 256 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;
	int sum=0;
	int i;
	char x[100]={0, };

	printf("입력할 숫자의 자리수를 입력하시오: ");
	scanf("%d", &n);
	scanf("%s", x);

	for (i=0; i<n; i++) {
		sum += x[i] - '0';
	}

	printf("%d", sum);
	

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

