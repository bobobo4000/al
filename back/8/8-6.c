// 문제
// ***********************************************************************************************************************************************************************************
// 영어  대소문자와 띄어쓰기만으로 이루어진 문자열이 주어진다. 이 문자열에는 몇 개의 단어가 있을까?
// 이를 구하는 프로그램을 작성하시오. 단, 한 단어가 여러 번 등장하면 등장한 횟수만큼 모두 세어야 한다.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 첫 줄에 영어 대소문자와 띄어쓰기로 이루어진 문자열이 주어진다. 
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 단어의 개수를 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// 문자열의 길이는 1,000,000을 넘지 않는다.			띄어쓰기는 한 개로 구분			공백이 연속해서 나오는 경우는 없다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		
// ***********************************************************************************************************************************************************************************
// The Curious Case of Benjamin Button
//  Mazatneunde Wae Teullyeoyo
// ***********************************************************************************************************************************************************************************

// 예제 출력																		
// ***********************************************************************************************************************************************************************************
// 6
// 3
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 2초
// 메모리 제한 : 128 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	char str[1000000]={0, };
	int i, cnt=0;

	gets(str);

	for (i=0; i<strlen(str); i++) {
		if (str[i] != ' ') {
			if (i>0 && str[i-1] != ' ') {
				continue;
			} else {
				cnt++;
			}
		}
	}
	printf("%d\n", cnt);
	
	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}