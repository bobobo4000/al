// 문제
// ***********************************************************************************************************************************************************************************
// 세준이는 기말고사를 망쳤다. 세준이는 점수를 조작해서 집에 가져가기로 했다. 일단 세준이는 자기 점수 중에 최댓값을 골랐다. 이 값을 M이라고 한다.
// 그리고 나서 모든 점수를 점수/M*100으로 고쳤다.
// - 예를 들어, 세준이의 최고점이 70이고, 수학점수가 50이었으면 수학점수는 50/70*100이 되어 71.43점이 된다.
// - 세준이의 성적을 위의 방법대로 새로 계산했을 때, 새로운 평균을 구하는 프로그램을 작성하시오.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 시험 본 과목의 개수 N이 주어진다. 
// 둘째 줄에 세준이의 현재 성적이 주어진다.
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에 새로운 평균을 출력한다. 실제 정답과 출력값의 절대오차 또는 상대오차가 10^-2 이하이면 정답이다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// 첫째 줄 값은 1000보다 작거나 같다.
// 둘째 줄 값은 100보다 작거나 같은 음이 아닌 정수이고, 적어도 하나의 값은 0보다 크다.
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 3																					 | 75.0
// 40 80 60																		 | 
//
// 3																					 | 66.6666667
// 10 20 30																		 | 
//
// 4																					 | 75.25
// 1 100 100 100															 | 
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 2초
// 메모리 제한 : 128 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n;
	int i;
	int score[1000]={0}; // 배열의 초기화(현재 점수)
	float new=0; // 배열의 초기화(새로운 점수)
	int total=0;
	int max=-100;

	printf("시험 본 과목의 개수: ");
	scanf("%d", &n);

	for (i=0; i<n; i++) {
		scanf("%d", &score[i]);
		if (score[i] > max) {
			max = score[i];
		}
	}

	for (i=0; i<n; i++) {
		new += (float)score[i]/max*100;
	}

	printf("새로운 평균 = %f", (float)new/n);

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

// 1. 시험 본 과목의 개수를 입력
// 2. 각 과목의 점수를 입력하여 배열(score[])에 저장
// 3. 배열에 저장된 값 중 최댓값이 무언인지 찾아서 또 다른 변수에 저장
// 4. 배열에 저장된 값을 주어진 공식(점수/M*100) 에 맞게 조정하여 저장
// 5. 조정된 값들을 이용하여 평균을 구한다.
// 6. 구한 평균 값을 출력한다.