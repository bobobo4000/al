// 문제
// ***********************************************************************************************************************************************************************************
// 대학생 새내기들의 90%는 자신이 반에서 평균은 넘는다고 생각한다. 당신은 그들에게 슬픈 진실을 알려줘야 한다.
// ***********************************************************************************************************************************************************************************

// 입력
// ***********************************************************************************************************************************************************************************
// 첫째 줄에는 테스트 케이스의 개수 C가 주어진다.
// 둘째 줄에는 각 테스트 케이스 마다 학생의 수 N이 첫 수로 주어지고, 이어서 N명의 점수가 주어진다.
// ***********************************************************************************************************************************************************************************

// 출력
// ***********************************************************************************************************************************************************************************
// 각 케이스마다 한 줄씩 평균을 넘는 학생들의 비율을 반올림하여 소수점 셋째 자리까지 출력한다.
// ***********************************************************************************************************************************************************************************

// 제한
// ***********************************************************************************************************************************************************************************
// (1 <= N <= 1000, N은 정수), 0 <= score <= 100
// ***********************************************************************************************************************************************************************************

// 예제 입력																		| 예제 출력
// ***********************************************************************************************************************************************************************************
// 5																					 |
// 5 50 50 70 80 100													 | 40.000 %
// 7 100 95 90 80 70 60 50										 | 57.143 %
// 3 70 90 80																	 | 33.333 %
// 3 70 90 81																	 | 66.667 %
// 9 100 99 98 97 96 95 94 93 91							 | 55.556 %
// ***********************************************************************************************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 256 MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

	int n, m, i, j;
	int score[1000] = {0};
	int sum=0, avg=0;
	int cnt=0;
	float allavg=0;

	printf("학과 수를 입력하세요: ");
	scanf("%d", &n);

	for (i=0; i<n; i++) {
		printf("학생 수를 입력하세요: ");
		scanf("%d", &m);
		
		for (j=0; j<m; j++) {
			printf("학생들의 점수를 입력하세요: ");
			scanf("%d", &score[j]);
			sum += score[j];
		}
		avg = sum/m;
		
		for (j=0; j<m; j++) {
			if (avg < score[j]) {
				cnt++;
			}
		}

		allavg = (float)cnt/m * 100;
		printf("학과당 학과 평균을 넘는 학생의 비율 = %.3f %%\n", allavg);

	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}

// 1. 학과 수를 입력
// 2. 학과별로 인원을 입력 후 각 학생들의 점수를 입력
// 3. 학과 학생들의 총점과 평균을 구한다.
// 4. 학과 평균과 각 학생들의 점수를 비교
// 5. 학과 평균보다 높은 학생의 비율을 산정
// 6. 비율 산정 결과를 출력