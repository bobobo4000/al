// 문제
// ****************************************************************************************************
// 흔한 수학 문제 중 하나는 주어진 점이 어느 사분면에 속하는지 알아내는 것이다.
// 사분면은 아래 그림처럼 1부터 4까지 번호를 갖는다.
// "Quadrant n"은 "제 n사분면"이라는 뜻이다.
//												y
//		Quadrant2						|    				Quadrant1
//												|
//		B(-12, 5) ●					|						● A(12, 5)
// x--------------------------------------------------
//		B(-12, -5) ●				|						●	D(12, -5)	
//												|
//		Quadrant3						|		 				Quadrant4
//
// 예를 들어, 좌표가 (12, 5)인 점 A는 x좌표와 y좌표가 모두 양수이므로 제 1사분면에 속한다.
// 점 B는 x좌표가 음수이고 y좌표가 양수이므로 제 2사분면에 속한다.
// 점의 좌표를 입력받아 그 점이 어느 사분면에 속하는지 알아내는 프로그램을 작성하시오.
// 단, x좌표와 y좌표는 모두 양수나 음수라고 가정한다.
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫 줄에는 정수 x가 주어진다., 다음줄에는 정수 y가 주어진다.
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 점 (x, y)의 사분문 번호(1, 2, 3, 4 중 하나)를 출력한다.
// ****************************************************************************************************

// 제한
// ****************************************************************************************************
// -1000 <= x <= 1000, -1000 <= y <= 1000
// ****************************************************************************************************

// 예제 입력																		| 예제 출력
// ****************************************************************************************************
// 12 5																				 | 1
// 9 -13																			 | 4
// ****************************************************************************************************

// 시간 제한 : 초
// 메모리 제한 : MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

  int x, y;

	printf("두 좌표를 입력하세요: ");
	scanf("%d %d", &x, &y);

	if (x > 0 && y > 0) {
		printf("제 1사분면");
	} else if (x < 0 && y > 0) {
		printf("제 2사분면");
	} else if (x < 0 && y < 0) {
		printf("제 3사분면");
	} else {
		printf("제 4사분면");
	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n\n소요된 시간 : %.3f \n", res);
}