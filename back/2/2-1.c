// 문제
// ****************************************************************************************************
// 두 정수 A와 B가 주어졌을 때, A와 B를 비교하는 프로그램을 작성하시오.
// ****************************************************************************************************

// 입력
// ****************************************************************************************************
// 첫째 줄에 A와 B가 주어진다. A와 B는 공백 한 칸으로 구분되어져 있다.
// ****************************************************************************************************

// 출력
// ****************************************************************************************************
// 첫째 줄에 다음 세 가지 중 하나를 출력한다.
// - A가 B보다 큰 경우에는 '>'를 출력한다.
// - A가 B보다 작은 경우에는 '<'를 출력한다.
// - A와 B가 같은 경우에는 '=='를 출력한다.
// ****************************************************************************************************

// 제한
// ****************************************************************************************************
// -10,000 <= a, b <= 10,000
// ****************************************************************************************************

// 예제 입력																		| 예제 출력
// ****************************************************************************************************
// 1 2																				 | <
// 10 2																				 | >
// 5 5																				 | ==
// ****************************************************************************************************

// 시간 제한 : 1초
// 메모리 제한 : 512MB

#include "header.h"

int main() {
	clock_t start, end;
	float res;

	start = clock();

  int a, b;
 	while (1) {
		scanf("%d %d", &a, &b);

		if (-10000 <= a <= 10000 && -10000 <= b <= 10000) {
			if (a > b) {
				printf(">");
				break;
			} else if (a == b) {
				printf("==");
				break;
			} else {
				printf("<");
				break;
			}	
		} else {
			printf("정수는 -10000 ~ 10000 사이 값만 입력할 수 있습니다.\n");
		}
 	}

	end = clock();
	res = (float)(end - start)/CLOCKS_PER_SEC;

	printf("\n소요된 시간 : %.3f \n", res);
}